/**
 * Run at https://www.qurananalysis.com/analysis/basic-statistics.php?lang=EN
 */
// eslint-disable-next-line no-unused-vars
function run() {
    const json = []
    // eslint-disable-next-line no-undef
    const table = document.querySelectorAll('.analysis-table')[1]
    Array.from(table.children[1].children).forEach((elem, i) => {
        json.push({
            chapter: i + 1,
            name: elem.cells[1].innerText,
            maxVerse: parseInt(elem.cells[2].innerText, 10),
        })
    })
    return json
}

/**
 * Will return an array of paginated parts for example
 * `paginate(286, 10)` will return:
 * - `"1-10"`
 * - `"11-20"`
 * - `"21-30"`
 * - `"31-40"`
 * - etc
 */
// eslint-disable-next-line no-unused-vars
function paginate(maxNumber, divideBy) {
    const upTo = maxNumber / divideBy
    const leftOver = maxNumber % divideBy
    const response = []

    for (let i = 1; i <= upTo; i += 1) {
        const from = i * 10 - 10 + 1
        const til = i * 10
        const part = `${from}-${til}`
        response.push(part)
        if (i === Math.floor(upTo) && leftOver > 0) {
            // eslint-disable-next-line no-underscore-dangle
            const _from = (i + 1) * 10 - 10 + 1
            // eslint-disable-next-line no-underscore-dangle
            const _til = i * 10 + leftOver
            const leftOverPart = `${_from}-${_til}`
            response.push(leftOverPart)
        }
    }

    if (response.length === 0) {
        /**
         * i.e. its less than 10
         */
        response.push(`1-${maxNumber}`)
    }

    return response
}

